package org.universidad.intro.Variables;

public class DeclaracionVariables {
    public static void main(String[] args) {
        int miNumeroEntero = 56;
        System.out.println("miNumeroEntero = " + miNumeroEntero);

        // Declaracion de un objeto de tipo String
        String nombre = "Jose Silva";
        System.out.println("nombre = " + nombre);
        nombre = "Aleco";
        System.out.println("nombre = " + nombre);
    }
}
